package semana2.exercicio15;

public class Ponto {
    private double x;
    private double y;

    public Ponto(double coordX, double coordY) {
        this.x = coordX;
        this.y = coordY;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
